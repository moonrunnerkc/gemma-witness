//! Day 2 evaluation harness.
//!
//! Walks every transcript in `tests/fixtures/transcripts/`, asks the sidecar
//! to structure it, and writes a Markdown report with per-row results,
//! aggregate validity, latency percentiles, and a confusion matrix on
//! `incident_type`.

use std::collections::BTreeMap;
use std::path::{Path, PathBuf};

use clap::Parser;
use serde::Deserialize;
use witness_inference::{InferenceClient, DEFAULT_ENDPOINT};

#[derive(Debug, Parser)]
#[command(
    name = "witness-eval",
    about = "Run Day 2 structured-extraction evaluation."
)]
struct Cli {
    /// Directory containing `*.txt` transcripts and `expected.json`.
    #[arg(long)]
    transcripts_dir: PathBuf,
    /// Path to the JSON Schema.
    #[arg(long)]
    schema: PathBuf,
    /// Sidecar endpoint.
    #[arg(long, default_value = DEFAULT_ENDPOINT)]
    endpoint: String,
    /// Where to write the Markdown report.
    #[arg(long)]
    output: PathBuf,
}

#[derive(Debug, Deserialize)]
struct ExpectedRow {
    transcript: String,
    incident_type: String,
    source: String,
}

#[derive(Debug)]
struct EvalRow {
    name: String,
    source: String,
    expected_type: String,
    predicted_type: String,
    schema_valid: bool,
    retries_used: u32,
    latency_ms: u128,
    error: Option<String>,
}

#[tokio::main]
async fn main() -> anyhow::Result<()> {
    tracing_subscriber::fmt()
        .with_env_filter(
            tracing_subscriber::EnvFilter::try_from_default_env()
                .unwrap_or_else(|_| tracing_subscriber::EnvFilter::new("info")),
        )
        .with_writer(std::io::stderr)
        .init();

    let cli = Cli::parse();
    let schema_raw = std::fs::read_to_string(&cli.schema)?;
    let schema: serde_json::Value = serde_json::from_str(&schema_raw)?;

    let expected_path = cli.transcripts_dir.join("expected.json");
    let expected_raw = std::fs::read_to_string(&expected_path)?;
    let expected: Vec<ExpectedRow> = serde_json::from_str(&expected_raw)?;

    let client = InferenceClient::with_endpoint(&cli.endpoint)?;
    let mut rows = Vec::new();
    for row in &expected {
        let transcript_path = cli.transcripts_dir.join(&row.transcript);
        let transcript = std::fs::read_to_string(&transcript_path)?;
        eprintln!("eval: {} ...", row.transcript);
        let started = std::time::Instant::now();
        let result = client.structure_incident(&transcript, &schema).await;
        let latency_ms = started.elapsed().as_millis();
        let entry = match result {
            Ok(outcome) => EvalRow {
                name: row.transcript.clone(),
                source: row.source.clone(),
                expected_type: row.incident_type.clone(),
                predicted_type: serde_json::to_value(&outcome.report.incident_type)
                    .ok()
                    .and_then(|v| v.as_str().map(str::to_string))
                    .unwrap_or_else(|| "unknown".to_string()),
                schema_valid: true,
                retries_used: outcome.retries_used,
                latency_ms: outcome.latency_ms,
                error: None,
            },
            Err(err) => EvalRow {
                name: row.transcript.clone(),
                source: row.source.clone(),
                expected_type: row.incident_type.clone(),
                predicted_type: "INVALID".to_string(),
                schema_valid: false,
                retries_used: 3,
                latency_ms,
                error: Some(format!("{err}")),
            },
        };
        eprintln!(
            "  -> valid={} retries={} latency_ms={}",
            entry.schema_valid, entry.retries_used, entry.latency_ms
        );
        rows.push(entry);
    }

    let markdown = render_markdown(&rows);
    std::fs::write(&cli.output, markdown)?;
    eprintln!("wrote {}", cli.output.display());

    let valid = rows.iter().filter(|r| r.schema_valid).count();
    println!(
        "VALIDITY: {}/{} ({}%)",
        valid,
        rows.len(),
        valid * 100 / rows.len().max(1)
    );
    Ok(())
}

fn render_markdown(rows: &[EvalRow]) -> String {
    let mut out = String::new();
    out.push_str("# Day 2 evaluation results\n\n");
    out.push_str("Generated by `cargo run -p witness-eval`.\n\n");
    out.push_str("## Per-transcript results\n\n");
    out.push_str(
        "| transcript | source | expected | predicted | valid | retries | latency_ms | error |\n",
    );
    out.push_str("|---|---|---|---|---|---|---|---|\n");
    for r in rows {
        out.push_str(&format!(
            "| {} | {} | {} | {} | {} | {} | {} | {} |\n",
            r.name,
            short(&r.source, 60),
            r.expected_type,
            r.predicted_type,
            r.schema_valid,
            r.retries_used,
            r.latency_ms,
            r.error.as_deref().map(|e| short(e, 80)).unwrap_or_default()
        ));
    }

    let n = rows.len().max(1);
    let valid = rows.iter().filter(|r| r.schema_valid).count();
    out.push_str(&format!(
        "\n## Aggregate\n\n- transcripts: {}\n- schema-valid: {} ({}%)\n",
        n,
        valid,
        valid * 100 / n
    ));

    let mut retry_dist: BTreeMap<u32, usize> = BTreeMap::new();
    for r in rows {
        *retry_dist.entry(r.retries_used).or_insert(0) += 1;
    }
    out.push_str("- retry distribution:\n");
    for (k, v) in &retry_dist {
        out.push_str(&format!("  - {} retries: {}\n", k, v));
    }

    let mut latencies: Vec<u128> = rows.iter().map(|r| r.latency_ms).collect();
    latencies.sort_unstable();
    let p50 = percentile(&latencies, 50);
    let p95 = percentile(&latencies, 95);
    out.push_str(&format!(
        "- latency p50: {} ms\n- latency p95: {} ms\n",
        p50, p95
    ));

    out.push_str("\n## Confusion matrix (incident_type)\n\n");
    let labels = collect_labels(rows);
    out.push_str("| expected \\ predicted |");
    for l in &labels {
        out.push_str(&format!(" {} |", l));
    }
    out.push_str("\n|---|");
    for _ in &labels {
        out.push_str("---|");
    }
    out.push('\n');
    for actual in &labels {
        out.push_str(&format!("| {} |", actual));
        for pred in &labels {
            let count = rows
                .iter()
                .filter(|r| &r.expected_type == actual && &r.predicted_type == pred)
                .count();
            out.push_str(&format!(" {} |", count));
        }
        out.push('\n');
    }
    out
}

fn collect_labels(rows: &[EvalRow]) -> Vec<String> {
    let mut set: std::collections::BTreeSet<String> = std::collections::BTreeSet::new();
    for r in rows {
        set.insert(r.expected_type.clone());
        set.insert(r.predicted_type.clone());
    }
    set.into_iter().collect()
}

fn percentile(sorted: &[u128], p: usize) -> u128 {
    if sorted.is_empty() {
        return 0;
    }
    let idx = (sorted.len() * p / 100).min(sorted.len() - 1);
    sorted[idx]
}

fn short(s: &str, n: usize) -> String {
    let trimmed = s.replace('\n', " ");
    if trimmed.chars().count() <= n {
        trimmed
    } else {
        let mut out: String = trimmed.chars().take(n - 1).collect();
        out.push('…');
        out
    }
}

#[allow(dead_code)]
fn _path_helper(p: &Path) -> &Path {
    p
}
